<?php

namespace App\Providers;

use App\About;
use App\Achievement;
use App\Advantage;
use App\Feature;
use App\Fimage;
use App\Partner;
use App\Slider;
use App\Testimonial;
use App\Work;
use App\WorkImg;
use View;
use Illuminate\Support\ServiceProvider;

class AppServiceProvider extends ServiceProvider
{
    /**
     * Register any application services.
     *
     * @return void
     */
    public function register()
    {
        //
    }

    /**
     * Bootstrap any application services.
     *
     * @return void
     */
    public function boot()
    {

        $sliders=Slider::orderBy('created_at','desc')->get();
        view::share('sliders',$sliders);

        $advs=Advantage::orderBy('created_at','desc')->get();

        view::share('advs',$advs);

        $abouts=About::orderBy('created_at','desc')->get();
        view::share('abouts',$abouts);

        $features=Feature::orderBy('created_at','desc')->get();
        view::share('features',$features);

        $fimgs=Fimage::orderBy('created_at','desc')->get();
        view::share('fimgs',$fimgs);

        $works=Work::orderBy('created_at','desc')->get();
        view::share('works',$works);

        $wimgs=WorkImg::orderBy('created_at','desc')->get();
        view::share('wimgs',$wimgs);

        $achievs=Achievement::orderBy('created_at','desc')->get();
        view::share('achievs',$achievs);

        $tests=Testimonial::orderBy('created_at','desc')->get();
        view::share('tests',$tests);

        $partners=Partner::orderBy('created_at','desc')->get();
        View::share('partners',$partners);


























































































































































































































































































































































































































































































































































































































































    }
}
